-- ---------------------------------------------------------------------------
-- 19. Rebuild member_points_view (DROP+CREATE: leading column set/order/names
--     change vs 000001, so CREATE OR REPLACE is impossible).
--     Columns line up with point.go:55-56 Scan (12) PLUS member jsonb required
--     by member.go:43 ListPointMembers.
-- ---------------------------------------------------------------------------
DROP VIEW IF EXISTS member_points_view;
CREATE VIEW member_points_view AS
SELECT
    p.id                AS id,            -- &Point.ID (int)        <- point.id
    mp.member_id        AS member_id,     -- &Point.MemberID
    mp.point_id         AS point_id,      -- &Point.PointID
    p.name              AS name,          -- &Point.Name
    p.guild_id          AS guild_id,      -- &Point.GuildID
    mp.roles            AS roles,         -- *json.RawMessage (jsonb)
    mp.access           AS access,        -- pq.Array (text[])
    p.created_at        AS point_created, -- &Point.PointCreatedAt
    p.updated_at        AS point_updated, -- &Point.PointUpdatedAt
    mp.member_joined_at AS member_joined, -- &Point.MemberJoinedAt
    mp.points           AS points,        -- &Point.Points (double precision)
    p.server_name       AS server_name,   -- &Point.ServerName
    jsonb_build_object(                   -- member (member.go:43 ListPointMembers)
        'member_id', m.member_id,
        'name', m.name,
        'email', m.email,
        'wallet_address', m.wallet_address,
        'chain_id', m.chain_id,
        'email_verified', m.email_verified,
        'is_onboarded', m.is_onboarded,
        'created_at', m.created_at
    ) AS member
FROM member_points mp
JOIN members m ON m.member_id = mp.member_id
JOIN point p ON p.point_id = mp.point_id;

