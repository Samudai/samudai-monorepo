-- ---------------------------------------------------------------------------
-- 1. Rename table points -> point (guarded; preserves member_points FK).
-- ---------------------------------------------------------------------------
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'points')
       AND NOT EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'point') THEN
        ALTER TABLE points RENAME TO point;
    END IF;
END $$;

-- Fresh-DB path: 000001 created "points"; on a brand-new DB the rename above
-- handles it. This guarantees "point" exists either way.
CREATE TABLE IF NOT EXISTS point (
    point_id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    name VARCHAR(255),
    description TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

